def _r2_score(y: np.ndarray, yhat: np.ndarray) -> float:
    y = np.asarray(y, dtype=float)
    yhat = np.asarray(yhat, dtype=float)
    mask = np.isfinite(y) & np.isfinite(yhat)
    y = y[mask]
    yhat = yhat[mask]
    if y.size < 2:
        return float("nan")
    ss_res = float(np.sum((y - yhat) ** 2))
    ss_tot = float(np.sum((y - np.mean(y)) ** 2))
    if ss_tot == 0:
        return float("nan")
    return 1.0 - ss_res / ss_tot


def _hb_model(gamma: np.ndarray, tauy: float, K: float, n: float) -> np.ndarray:
    return tauy + K * np.power(gamma, n)


def fit_herschel_bulkley(gamma: np.ndarray, tau: np.ndarray, tauy0: Optional[float] = None) -> Dict[str, float]:
    """
    Fit τ = τy + K γ̇^n (Herschel–Bulkley) by non-linear least squares.
    Returns tau_y_Pa, K, n, r_squared, n_points.
    """
    gamma = np.asarray(gamma, dtype=float)
    tau = np.asarray(tau, dtype=float)

    mask = (gamma > 0) & np.isfinite(gamma) & np.isfinite(tau)
    g = gamma[mask]
    t = tau[mask]
    if g.size < 4:
        return {"tau_y_Pa": float("nan"), "K": float("nan"), "n": float("nan"), "r_squared": float("nan"), "n_points": int(g.size)}

    tauy0 = float(tauy0) if (tauy0 is not None and np.isfinite(tauy0)) else float(max(0.0, np.nanmin(t)))
    if tauy0 >= float(np.nanmax(t)):
        tauy0 = float(max(0.0, np.nanmin(t) * 0.5))

    tp = t - tauy0
    mask2 = (tp > 0) & (g > 0)
    if np.sum(mask2) >= 3:
        n_guess, logK_guess = np.polyfit(np.log(g[mask2]), np.log(tp[mask2]), 1)
        K0 = float(np.exp(logK_guess))
        n0 = float(max(1e-6, n_guess))
    else:
        K0 = float(np.nanmax(np.maximum(tp, 1e-12)))
        n0 = 1.0

    p0 = [tauy0, max(1e-12, K0), max(1e-6, n0)]
    bounds = ([0.0, 0.0, 0.0], [np.inf, np.inf, np.inf])

    try:
        popt, _pcov = curve_fit(_hb_model, g, t, p0=p0, bounds=bounds, maxfev=100000)
        tauy, K, n = popt
    except Exception:
        return {"tau_y_Pa": float("nan"), "K": float("nan"), "n": float("nan"), "r_squared": float("nan"), "n_points": int(g.size)}

    t_hat = _hb_model(g, tauy, K, n)
    r2 = _r2_score(t, t_hat)
    return {"tau_y_Pa": float(tauy), "K": float(K), "n": float(n), "r_squared": float(r2), "n_points": int(g.size)}


def _cross_model_tau_eta(tau: np.ndarray, eta_inf: float, delta_eta: float, sigma_c: float, m: float) -> np.ndarray:
    """Modified Cross model written against stress: η = η∞ + (η0-η∞)/(1+(τ/τc)^m)."""
    tau = np.asarray(tau, dtype=float)
    return eta_inf + delta_eta / (1.0 + np.power(tau / sigma_c, m))


def _ellis_model_tau_eta(tau: np.ndarray, eta0: float, sigma_c: float, m: float) -> np.ndarray:
    """Ellis model: η = η0/(1+(τ/τc)^m)."""
    tau = np.asarray(tau, dtype=float)
    return eta0 / (1.0 + np.power(tau / sigma_c, m))


def _prepare_eta_tau(tau: np.ndarray, eta: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    tau = np.asarray(tau, dtype=float)
    eta = np.asarray(eta, dtype=float)
    mask = (tau > 0) & (eta > 0) & np.isfinite(tau) & np.isfinite(eta)
    x = tau[mask]
    y = eta[mask]
    if x.size == 0:
        return x, y
    order = np.argsort(x)
    return x[order], y[order]


def _initial_sigma_c_from_midpoint(tau: np.ndarray, eta: np.ndarray, eta0: float, eta_inf: float) -> float:
    """Pick the stress nearest the logarithmic mid-viscosity as a stable τc guess."""
    tau = np.asarray(tau, dtype=float)
    eta = np.asarray(eta, dtype=float)
    if tau.size == 0:
        return 1.0
    eta0 = max(float(eta0), 1e-12)
    eta_inf = max(float(eta_inf), 1e-12)
    mid = math.sqrt(eta0 * eta_inf) if eta0 > eta_inf else np.nanmedian(eta)
    if not np.isfinite(mid) or mid <= 0:
        mid = np.nanmedian(eta)
    idx = int(np.nanargmin(np.abs(np.log(eta) - np.log(mid))))
    guess = float(tau[idx])
    if not np.isfinite(guess) or guess <= 0:
        guess = float(np.nanmedian(tau))
    return max(guess, 1e-12)


def fit_cross(tau: np.ndarray, eta: np.ndarray) -> Dict[str, float]:
    """
    Fit modified Cross model in log-viscosity space:
        η = η∞ + (η0 - η∞)/(1 + (τ/σc)^m)

    Returns eta0_Pa_s, eta_inf_Pa_s, sigma_c_Pa, m, r_squared, n_points.
    """
    x, y = _prepare_eta_tau(tau, eta)
    if x.size < 5:
        return {"eta0_Pa_s": float("nan"), "eta_inf_Pa_s": float("nan"), "sigma_c_Pa": float("nan"), "m": float("nan"), "r_squared": float("nan"), "n_points": int(x.size)}

    eta0_guess = float(np.nanpercentile(y, 95))
    eta_inf_guess = float(max(np.nanpercentile(y, 5), 1e-12))
    if eta0_guess <= eta_inf_guess:
        eta0_guess = float(np.nanmax(y))
        eta_inf_guess = float(max(np.nanmin(y), 1e-12))
    delta_guess = max(eta0_guess - eta_inf_guess, 1e-12)
    sigma_c_guess = _initial_sigma_c_from_midpoint(x, y, eta0_guess, eta_inf_guess)
    p0 = [eta_inf_guess, delta_guess, sigma_c_guess, 2.0]
    bounds = ([0.0, 0.0, max(float(np.nanmin(x)) * 1e-6, 1e-12), 0.01], [np.inf, np.inf, max(float(np.nanmax(x)) * 1e6, 1e-12), 100.0])

    def log_model(xx, eta_inf, delta_eta, sigma_c, m):
        return np.log(np.maximum(_cross_model_tau_eta(xx, eta_inf, delta_eta, sigma_c, m), 1e-300))

    try:
        popt, _pcov = curve_fit(log_model, x, np.log(y), p0=p0, bounds=bounds, maxfev=200000)
        eta_inf, delta_eta, sigma_c, m = popt
    except Exception:
        return {"eta0_Pa_s": float("nan"), "eta_inf_Pa_s": float("nan"), "sigma_c_Pa": float("nan"), "m": float("nan"), "r_squared": float("nan"), "n_points": int(x.size)}

    yhat = _cross_model_tau_eta(x, eta_inf, delta_eta, sigma_c, m)
    r2 = _r2_score(np.log(y), np.log(np.maximum(yhat, 1e-300)))
    return {"eta0_Pa_s": float(eta_inf + delta_eta), "eta_inf_Pa_s": float(eta_inf), "sigma_c_Pa": float(sigma_c), "m": float(m), "r_squared": float(r2), "n_points": int(x.size)}


def fit_ellis(tau: np.ndarray, eta: np.ndarray) -> Dict[str, float]:
    """
    Fit Ellis model in log-viscosity space:
        η = η0/(1 + (τ/σc)^m)

    Returns eta0_Pa_s, sigma_c_Pa, m, r_squared, n_points.
    eta_inf_Pa_s is reported as 0.0 for consistency with the Cross table.
    """
    x, y = _prepare_eta_tau(tau, eta)
    if x.size < 4:
        return {"eta0_Pa_s": float("nan"), "eta_inf_Pa_s": 0.0, "sigma_c_Pa": float("nan"), "m": float("nan"), "r_squared": float("nan"), "n_points": int(x.size)}

    eta0_guess = float(np.nanpercentile(y, 95))
    sigma_c_guess = _initial_sigma_c_from_midpoint(x, y, eta0_guess, max(float(np.nanpercentile(y, 5)), 1e-12))
    p0 = [max(eta0_guess, 1e-12), sigma_c_guess, 2.0]
    bounds = ([0.0, max(float(np.nanmin(x)) * 1e-6, 1e-12), 0.01], [np.inf, max(float(np.nanmax(x)) * 1e6, 1e-12), 100.0])

    def log_model(xx, eta0, sigma_c, m):
        return np.log(np.maximum(_ellis_model_tau_eta(xx, eta0, sigma_c, m), 1e-300))

    try:
        popt, _pcov = curve_fit(log_model, x, np.log(y), p0=p0, bounds=bounds, maxfev=200000)
        eta0, sigma_c, m = popt
    except Exception:
        return {"eta0_Pa_s": float("nan"), "eta_inf_Pa_s": 0.0, "sigma_c_Pa": float("nan"), "m": float("nan"), "r_squared": float("nan"), "n_points": int(x.size)}

    yhat = _ellis_model_tau_eta(x, eta0, sigma_c, m)
    r2 = _r2_score(np.log(y), np.log(np.maximum(yhat, 1e-300)))
    return {"eta0_Pa_s": float(eta0), "eta_inf_Pa_s": 0.0, "sigma_c_Pa": float(sigma_c), "m": float(m), "r_squared": float(r2), "n_points": int(x.size)}


