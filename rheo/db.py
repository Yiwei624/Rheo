def delete_experiment(db_path: str | Path, experiment_id: int) -> None:
    """Delete an experiment and all dependent rows.

    Thanks to ON DELETE CASCADE constraints, this will automatically remove:
    - measurements
    - fit_results
    - derived_metrics
    - artifacts

    Note: this only deletes database rows. Any files on disk (outputs/, uploads/)
    are not removed here; the Streamlit UI can optionally clean them up.
    """
    db_path = Path(db_path)
    if not db_path.exists():
        raise FileNotFoundError(db_path)
    conn = connect(db_path)
    try:
        cur = conn.cursor()
        cur.execute("DELETE FROM experiments WHERE id=?", (int(experiment_id),))
        conn.commit()
    finally:
        conn.close()


