def connect(db_path: str | Path) -> sqlite3.Connection:
    conn = sqlite3.connect(str(db_path))
    conn.execute("PRAGMA foreign_keys = ON;")
    return conn


def init_db(db_path: str | Path, schema_path: str | Path) -> None:
    """Create tables if they don't exist and upgrade older DBs when needed."""
    db_path = Path(db_path)
    schema_path = Path(schema_path)
    conn = connect(db_path)
    try:
        sql = schema_path.read_text(encoding="utf-8")
        conn.executescript(sql)
        _ensure_fit_results_columns(conn)
        conn.commit()
    finally:
        conn.close()


def _ensure_fit_results_columns(conn: sqlite3.Connection) -> None:
    """Add Cross/Ellis columns if an older database already has fit_results.

    SQLite cannot add multiple columns in one ALTER statement, so we add them
    one by one. Older, now-unused fit columns may remain in existing databases,
    but the app no longer writes or displays removed models.
    """
    existing = {row[1] for row in conn.execute("PRAGMA table_info(fit_results)").fetchall()}
    required = {
        "eta0_Pa_s": "REAL",
        "eta_inf_Pa_s": "REAL",
        "sigma_c_Pa": "REAL",
        "m": "REAL",
    }
    for name, typ in required.items():
        if name not in existing:
            conn.execute(f"ALTER TABLE fit_results ADD COLUMN {name} {typ}")


