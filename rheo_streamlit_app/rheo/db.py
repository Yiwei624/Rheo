def _norm_header(s: str) -> str:
    s = str(s).strip().lower()
    # normalize degree symbol variants
    s = s.replace("℃", "c").replace("°c", "c").replace("°", "")
    # remove common separators/units punctuation
    s = re.sub(r"[ \t\n\r/·\-\(\)\[\]\{\}:;%]", "", s)
    return s


def normalize_columns(df: pd.DataFrame) -> pd.DataFrame:
    """
    Map a rheometer-export dataframe to canonical columns used by this project.
    Expected raw headers include (English):
      Shear Rate 1/s, Shear Stress Pa, Viscosity Pas, Target Shear Rate 1/s,
      Percentage Deviation %, Temperature ℃, Time s, Thrust g, Accumulated Time s,
      Torque Nm, Angular Velocity rad/s, Notes
    """
    col_map: Dict[str, str] = {}
    for c in df.columns:
        k = _norm_header(c)
        target = None
        if ("target" in k and "shearrate" in k) or (k.startswith("target") and "rate" in k):
            target = "target_shear_rate_1_s"
        elif "shearrate" in k and "target" not in k:
            target = "shear_rate_1_s"
        elif "shearstress" in k:
            target = "shear_stress_Pa"
        elif "viscosity" in k:
            target = "viscosity_Pa_s"
        elif "deviation" in k:
            target = "percentage_deviation_pct"
        elif "temperature" in k:
            target = "temperature_C"
        elif k == "times" or k.startswith("time"):
            # "Time s"
            target = "time_s"
        elif "thrust" in k:
            target = "thrust_g"
        elif "accumulated" in k and "time" in k:
            target = "accumulated_time_s"
        elif "torque" in k:
            target = "torque_Nm"
        elif "angularvelocity" in k:
            target = "angular_velocity_rad_s"
        elif "notes" in k:
            target = "notes"

        if target is not None:
            col_map[c] = target

    df = df.rename(columns=col_map).copy()

    # Ensure all expected columns exist (missing => filled with NaN)
    expected = [
        "shear_rate_1_s",
        "shear_stress_Pa",
        "viscosity_Pa_s",
        "target_shear_rate_1_s",
        "percentage_deviation_pct",
        "temperature_C",
        "time_s",
        "thrust_g",
        "accumulated_time_s",
        "torque_Nm",
        "angular_velocity_rad_s",
        "notes",
    ]
    for c in expected:
        if c not in df.columns:
            df[c] = np.nan

    # Coerce numeric columns
    numeric_cols = [c for c in expected if c not in ("notes",)]
    for c in numeric_cols:
        df[c] = pd.to_numeric(df[c], errors="coerce")

    # Notes as string
    df["notes"] = df["notes"].astype(str).where(~df["notes"].isna(), "")
    return df[expected]


def read_data_file(path: str | Path, sheet_name: str | int | None = 0) -> pd.DataFrame:
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(path)

    if path.suffix.lower() in [".xlsx", ".xls"]:
        df = pd.read_excel(path, sheet_name=sheet_name)
    else:
        # auto-sep sniffing
        df = pd.read_csv(path, sep=None, engine="python")
    return df


