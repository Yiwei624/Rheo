def _insert_fit_result(conn: sqlite3.Connection, experiment_id: int, segment: str, model: str, fields: Dict[str, Any], method: str = "") -> None:
    cur = conn.cursor()
    cur.execute(
        """
        INSERT INTO fit_results(experiment_id, segment, model, tau_y_Pa, K, n, tau0_Pa, mu_Pa_s, r_squared, n_points, method)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        (
            experiment_id,
            segment,
            model,
            fields.get("tau_y_Pa", None),
            fields.get("K", None),
            fields.get("n", None),
            fields.get("tau0_Pa", None),
            fields.get("mu_Pa_s", None),
            fields.get("r_squared", None),
            fields.get("n_points", None),
            method,
        ),
    )


def _upsert_derived_metrics(conn: sqlite3.Connection, experiment_id: int, metrics: Dict[str, Any]) -> None:
    cur = conn.cursor()
    # delete then insert (simple upsert)
    cur.execute("DELETE FROM derived_metrics WHERE experiment_id=?", (experiment_id,))
    cur.execute(
        """
        INSERT INTO derived_metrics(
            experiment_id,
            hysteresis_area_Pa_s, hysteresis_area_method,
            tau1c_up_Pa, tau1c_down_Pa, tau1c_all_Pa,
            tauy_up_Pa, tauy_down_Pa, tauy_all_Pa
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
        """,
        (
            experiment_id,
            metrics.get("hysteresis_area_Pa_s", None),
            metrics.get("hysteresis_area_method", None),
            metrics.get("tau1c_up_Pa", None),
            metrics.get("tau1c_down_Pa", None),
            metrics.get("tau1c_all_Pa", None),
            metrics.get("tauy_up_Pa", None),
            metrics.get("tauy_down_Pa", None),
            metrics.get("tauy_all_Pa", None),
        ),
    )


def _insert_artifact(conn: sqlite3.Connection, experiment_id: int, kind: str, path: str) -> None:
    cur = conn.cursor()
    cur.execute(
        "INSERT INTO artifacts(experiment_id, kind, path) VALUES (?, ?, ?)",
        (experiment_id, kind, path),
    )


def analyze_experiment(
    db_path: str | Path,
    experiment_id: int,
    *,
    outdir: str | Path,
) -> Dict[str, Any]:
    """
    Full analysis:
    - load data
    - detect up/down
    - fit models
    - compute hysteresis area
    - compute tau1c and r0/r2 using HB τy and (r1,r2)
    - generate plots
    - store results + artifact paths in DB

    Returns a dict summary (also written to CSV).
    """
    conn = connect(db_path)
    try:
        exp = fetch_experiment(conn, experiment_id)
        df = load_measurements_df(conn, experiment_id)
        segments = split_up_down(df)

        # Write segments back to DB (for browsing)
        seg_by_idx: Dict[int, str] = {}
        if "all" in segments:
            for i in segments["all"]["row_index"].astype(int).tolist():
                seg_by_idx[int(i)] = "all"
        else:
            for seg_name, sdf in segments.items():
                for i in sdf["row_index"].astype(int).tolist():
                    seg_by_idx[int(i)] = seg_name
        update_segments(conn, experiment_id, seg_by_idx)

        # Clear prior fits/artifacts for this experiment (optional)
        conn.execute("DELETE FROM fit_results WHERE experiment_id=?", (experiment_id,))
        conn.execute("DELETE FROM artifacts WHERE experiment_id=?", (experiment_id,))
        conn.execute("DELETE FROM derived_metrics WHERE experiment_id=?", (experiment_id,))

        # Fits
        fit_rows: List[Dict[str, Any]] = []
        hb_tauy_by_seg: Dict[str, float] = {}

        for seg_name, sdf in segments.items():
            gamma = sdf["shear_rate_1_s"].to_numpy(dtype=float)
            tau = sdf["shear_stress_Pa"].to_numpy(dtype=float)

            # Power law
            pl = fit_power_law(gamma, tau)
            _insert_fit_result(conn, experiment_id, seg_name, "power_law", {"K": pl["K"], "n": pl["n"], "r_squared": pl["r_squared"], "n_points": pl["n_points"]}, method="log-linear")
            fit_rows.append({"segment": seg_name, "model": "power_law", **pl})

            # Bingham
            bm = fit_bingham(gamma, tau)
            _insert_fit_result(conn, experiment_id, seg_name, "bingham", {"tau0_Pa": bm["tau0_Pa"], "mu_Pa_s": bm["mu_Pa_s"], "r_squared": bm["r_squared"], "n_points": bm["n_points"]}, method="linear")
            fit_rows.append({"segment": seg_name, "model": "bingham", **bm})

            # HB (tauy initial guess from experiment metadata)
            hb = fit_herschel_bulkley(gamma, tau, tauy0=exp.get("yield_stress0_Pa", None))
            _insert_fit_result(conn, experiment_id, seg_name, "herschel_bulkley", {"tau_y_Pa": hb["tau_y_Pa"], "K": hb["K"], "n": hb["n"], "r_squared": hb["r_squared"], "n_points": hb["n_points"]}, method="curve_fit")
            fit_rows.append({"segment": seg_name, "model": "herschel_bulkley", **hb})

            hb_tauy_by_seg[seg_name] = hb["tau_y_Pa"]

        # Hysteresis area (if up/down)
        hysteresis_area = float("nan")
        hysteresis_method = ""
        if "up" in segments and "down" in segments:
            hysteresis_area, hysteresis_method = compute_hysteresis_area(segments["up"], segments["down"])
        # Partial yield metrics
        r1 = exp.get("r1_m", None)
        r2 = exp.get("r2_m", None)

        partial_info: Dict[str, Dict[str, Any]] = {}
        derived: Dict[str, Any] = {
            "hysteresis_area_Pa_s": hysteresis_area,
            "hysteresis_area_method": hysteresis_method,
        }

        if r1 is not None and r2 is not None and np.isfinite(r1) and np.isfinite(r2) and r1 > 0 and r2 > 0:
            for seg_name, sdf in segments.items():
                tauy = hb_tauy_by_seg.get(seg_name, float("nan"))
                tau1c, r0r2, mask = compute_partial_yield(sdf, tauy, float(r1), float(r2))
                partial_info[seg_name] = {"tau1c": tau1c, "r0_over_r2": r0r2, "partial_mask": mask}
                if seg_name == "up":
                    derived["tau1c_up_Pa"] = tau1c
                    derived["tauy_up_Pa"] = tauy
                elif seg_name == "down":
                    derived["tau1c_down_Pa"] = tau1c
                    derived["tauy_down_Pa"] = tauy
                elif seg_name == "all":
                    derived["tau1c_all_Pa"] = tau1c
                    derived["tauy_all_Pa"] = tauy

        _upsert_derived_metrics(conn, experiment_id, derived)

        # Plots
        exp_label = f"exp{experiment_id} — {exp['sample_name']}"
        artifacts = make_plots(df_all=df, segments=segments, outdir=outdir, exp_label=exp_label, partial_info=partial_info)

        for kind, path in artifacts.items():
            _insert_artifact(conn, experiment_id, kind, path)

        # Summary CSV
        outdir = Path(outdir)
        outdir.mkdir(parents=True, exist_ok=True)
        summary_df = pd.DataFrame(fit_rows)
        summary_csv = outdir / "fit_summary.csv"
        summary_df.to_csv(summary_csv, index=False)
        _insert_artifact(conn, experiment_id, "summary_csv", str(summary_csv))

        # A single "overview" Excel file: fits + derived metrics (incl. hysteresis area, tau1c, tauy)
        overview_xlsx = outdir / "results_overview.xlsx"
        derived_df = pd.DataFrame([derived])
        with pd.ExcelWriter(overview_xlsx, engine="openpyxl") as xw:
            summary_df.to_excel(xw, sheet_name="fit_results", index=False)
            derived_df.to_excel(xw, sheet_name="derived_metrics", index=False)
        _insert_artifact(conn, experiment_id, "overview_xlsx", str(overview_xlsx))

        conn.commit()

        return {
            "experiment": exp,
            "derived_metrics": derived,
            "fit_summary_csv": str(summary_csv),
            "artifacts": artifacts,
        }
    finally:
        conn.close()

