def make_geometry_schematic(
    geometry: str,
    r1_m: float,
    r2_m: float,
    *,
    title: str | None = None,
):
    """Create an illustrative concentric-geometry schematic for vane/cup or bob/cup.

    The drawing is scaled by the actual ratio r1/r2 and adds radius annotations,
    gap annotation, and a lightly shaded annulus so the geometry reads clearly
    inside the Streamlit UI.
    """
    if not (np.isfinite(r1_m) and np.isfinite(r2_m) and r1_m > 0 and r2_m > 0 and r2_m > r1_m):
        raise ValueError("Need valid radii with r2 > r1.")

    ratio = float(r1_m) / float(r2_m)
    r1_mm = float(r1_m) * 1000.0
    r2_mm = float(r2_m) * 1000.0
    gap_mm = (float(r2_m) - float(r1_m)) * 1000.0

    fig, ax = plt.subplots(figsize=(5.2, 5.2))
    ax.set_aspect("equal")
    ax.axis("off")

    # Outer cup wall
    outer = Circle((0.0, 0.0), 1.0, fill=False, linewidth=3.0)
    ax.add_patch(outer)
    # Annulus shading to make the gap easier to read visually
    annulus = Wedge((0.0, 0.0), 1.0, 0, 360, width=max(0.02, 1.0 - ratio), alpha=0.12)
    ax.add_patch(annulus)
    # Inner bob / vane envelope
    inner = Circle((0.0, 0.0), ratio, fill=True, alpha=0.18, linewidth=2.2)
    ax.add_patch(inner)
    inner_outline = Circle((0.0, 0.0), ratio, fill=False, linewidth=2.2)
    ax.add_patch(inner_outline)

    if geometry == "vane_cup":
        # Add simple spokes so the vane looks less like a plain cylinder.
        n_spokes = 8
        for ang in np.linspace(0, 2 * np.pi, n_spokes, endpoint=False):
            ax.plot([0.0, ratio * np.cos(ang)], [0.0, ratio * np.sin(ang)], linewidth=1.2)
        hub = Circle((0.0, 0.0), ratio * 0.16, fill=False, linewidth=1.2)
        ax.add_patch(hub)
        inner_label = "vane"
    else:
        inner_label = "bob"

    # Radius arrows and labels
    ang_r1 = np.deg2rad(35)
    ang_r2 = np.deg2rad(140)
    p1 = (ratio * np.cos(ang_r1), ratio * np.sin(ang_r1))
    p2 = (1.0 * np.cos(ang_r2), 1.0 * np.sin(ang_r2))
    ax.add_patch(FancyArrowPatch((0.0, 0.0), p1, arrowstyle="<->", mutation_scale=14, linewidth=1.3))
    ax.add_patch(FancyArrowPatch((0.0, 0.0), p2, arrowstyle="<->", mutation_scale=14, linewidth=1.3))
    ax.text(p1[0] * 0.62, p1[1] * 0.62 + 0.06, f"r1 = {r1_mm:.2f} mm", ha="center", va="center")
    ax.text(p2[0] * 0.66 - 0.03, p2[1] * 0.66 + 0.04, f"r2 = {r2_mm:.2f} mm", ha="center", va="center")

    # Gap annotation
    y_gap = -1.10
    ax.add_patch(FancyArrowPatch((ratio, y_gap), (1.0, y_gap), arrowstyle="<->", mutation_scale=14, linewidth=1.2))
    ax.text((ratio + 1.0) / 2.0, y_gap - 0.08, f"gap = {gap_mm:.2f} mm", ha="center", va="top")

    # Labels
    ax.text(0.0, 0.0, inner_label, ha="center", va="center")
    ax.text(0.0, 1.08, "cup wall", ha="center", va="bottom")
    ax.text(0.0, -1.28, f"r2 / r1 = {float(r2_m) / float(r1_m):.3f}", ha="center", va="top")

    ax.set_xlim(-1.35, 1.35)
    ax.set_ylim(-1.35, 1.35)
    if title:
        ax.set_title(title)
    return fig


