def _r2_score(y: np.ndarray, yhat: np.ndarray) -> float:
    y = np.asarray(y, dtype=float)
    yhat = np.asarray(yhat, dtype=float)
    mask = np.isfinite(y) & np.isfinite(yhat)
    y = y[mask]
    yhat = yhat[mask]
    if y.size < 2:
        return float("nan")
    ss_res = float(np.sum((y - yhat) ** 2))
    ss_tot = float(np.sum((y - np.mean(y)) ** 2))
    if ss_tot == 0:
        return float("nan")
    return 1.0 - ss_res / ss_tot


def fit_power_law(gamma: np.ndarray, tau: np.ndarray) -> Dict[str, float]:
    """
    Fit τ = K γ̇^n via linear regression in log space.
    Returns K, n, r_squared (in log space), n_points.
    """
    gamma = np.asarray(gamma, dtype=float)
    tau = np.asarray(tau, dtype=float)
    mask = (gamma > 0) & (tau > 0) & np.isfinite(gamma) & np.isfinite(tau)
    g = gamma[mask]
    t = tau[mask]
    if g.size < 3:
        return {"K": float("nan"), "n": float("nan"), "r_squared": float("nan"), "n_points": int(g.size)}

    lg = np.log(g)
    lt = np.log(t)
    n, logK = np.polyfit(lg, lt, 1)
    t_hat = np.exp(logK) * g ** n
    r2 = _r2_score(lt, np.log(t_hat))
    return {"K": float(np.exp(logK)), "n": float(n), "r_squared": float(r2), "n_points": int(g.size)}


def fit_bingham(gamma: np.ndarray, tau: np.ndarray) -> Dict[str, float]:
    """
    Fit τ = τ0 + μ γ̇ via least squares (linear regression).
    Returns tau0, mu, r_squared, n_points.
    """
    gamma = np.asarray(gamma, dtype=float)
    tau = np.asarray(tau, dtype=float)
    mask = np.isfinite(gamma) & np.isfinite(tau)
    g = gamma[mask]
    t = tau[mask]
    if g.size < 3:
        return {"tau0_Pa": float("nan"), "mu_Pa_s": float("nan"), "r_squared": float("nan"), "n_points": int(g.size)}

    mu, tau0 = np.polyfit(g, t, 1)  # slope, intercept
    t_hat = tau0 + mu * g
    r2 = _r2_score(t, t_hat)
    return {"tau0_Pa": float(tau0), "mu_Pa_s": float(mu), "r_squared": float(r2), "n_points": int(g.size)}


def _hb_model(gamma: np.ndarray, tauy: float, K: float, n: float) -> np.ndarray:
    return tauy + K * np.power(gamma, n)


def fit_herschel_bulkley(gamma: np.ndarray, tau: np.ndarray, tauy0: Optional[float] = None) -> Dict[str, float]:
    """
    Fit τ = τy + K γ̇^n (HB) by non-linear least squares (curve_fit).
    tauy0 is used as initial guess for τy.
    Returns tau_y_Pa, K, n, r_squared, n_points.
    """
    gamma = np.asarray(gamma, dtype=float)
    tau = np.asarray(tau, dtype=float)

    mask = (gamma > 0) & np.isfinite(gamma) & np.isfinite(tau)
    g = gamma[mask]
    t = tau[mask]
    if g.size < 4:
        return {"tau_y_Pa": float("nan"), "K": float("nan"), "n": float("nan"), "r_squared": float("nan"), "n_points": int(g.size)}

    # Initial guesses
    tauy0 = float(tauy0) if (tauy0 is not None and np.isfinite(tauy0)) else float(max(0.0, np.nanmin(t)))
    # Guard if tauy0 is too close to max(t), which makes (t - tauy0) nearly 0.
    if tauy0 >= float(np.nanmax(t)):
        tauy0 = float(max(0.0, np.nanmin(t) * 0.5))

    # Rough K,n guess from log fit of (tau - tauy0) ~ K gamma^n
    tp = t - tauy0
    mask2 = (tp > 0) & (g > 0)
    if np.sum(mask2) >= 3:
        n_guess, logK_guess = np.polyfit(np.log(g[mask2]), np.log(tp[mask2]), 1)
        K0 = float(np.exp(logK_guess))
        n0 = float(max(1e-6, n_guess))
    else:
        K0 = float(np.nanmax(tp)) if np.isfinite(np.nanmax(tp)) else 1.0
        n0 = 1.0

    p0 = [tauy0, max(1e-12, K0), max(1e-6, n0)]
    bounds = ([0.0, 0.0, 0.0], [np.inf, np.inf, np.inf])

    try:
        popt, _pcov = curve_fit(_hb_model, g, t, p0=p0, bounds=bounds, maxfev=100000)
        tauy, K, n = popt
    except Exception:
        return {"tau_y_Pa": float("nan"), "K": float("nan"), "n": float("nan"), "r_squared": float("nan"), "n_points": int(g.size)}

    t_hat = _hb_model(g, tauy, K, n)
    r2 = _r2_score(t, t_hat)
    return {"tau_y_Pa": float(tauy), "K": float(K), "n": float(n), "r_squared": float(r2), "n_points": int(g.size)}


