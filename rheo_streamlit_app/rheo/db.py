def split_up_down(df: pd.DataFrame, min_points_each: int = 5) -> Dict[str, pd.DataFrame]:
    """
    Detect a ramp-up then ramp-down sequence.

    Strategy:
    - Prefer target_shear_rate_1_s if present, else shear_rate_1_s.
    - Find the last index of the global maximum.
    - If both sides have at least `min_points_each`, return {'up':..., 'down':...}
      otherwise return {'all':...}
    """
    x = df["target_shear_rate_1_s"].copy()
    if x.isna().all():
        x = df["shear_rate_1_s"].copy()
    x = x.ffill().bfill()

    if len(df) < 2 * min_points_each:
        return {"all": df.copy()}

    max_val = float(np.nanmax(x.values))
    if not np.isfinite(max_val):
        return {"all": df.copy()}

    idxs = np.where(np.isclose(x.values, max_val, rtol=1e-12, atol=1e-12))[0]
    if len(idxs) == 0:
        return {"all": df.copy()}
    turn = int(idxs[-1])  # last occurrence of max

    up = df.iloc[: turn + 1].copy()
    down = df.iloc[turn + 1 :].copy()

    if len(up) >= min_points_each and len(down) >= min_points_each:
        return {"up": up, "down": down}
    return {"all": df.copy()}


