def _maybe_log_axes(ax, x: np.ndarray, y: Optional[np.ndarray] = None, *, xlog: bool = True, ylog: bool = False) -> None:
    """Apply log scaling if data are compatible.

    Rheometer exports often contain NaNs (e.g., unused columns, warm-up rows).
    For axis scaling we treat NaNs as missing values and only require that there
    exist *some* finite values and that the finite minimum is strictly positive.
    """

    # IMPORTANT:
    # Many rheometer exports contain *some* 0 values (e.g., pre-run rows).
    # Using nanmin over all finite values would then block log scaling.
    # Here we decide compatibility based on the existence of at least one
    # strictly-positive finite value.

    x = np.asarray(x, dtype=float)
    if xlog:
        fx = np.isfinite(x) & (x > 0)
        if np.any(fx):
            ax.set_xscale("log")

    if y is not None and ylog:
        y = np.asarray(y, dtype=float)
        fy = np.isfinite(y) & (y > 0)
        if np.any(fy):
            ax.set_yscale("log")


