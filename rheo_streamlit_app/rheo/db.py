def compute_hysteresis_area(
    up: pd.DataFrame,
    down: pd.DataFrame,
    *,
    x_col: str = "shear_rate_1_s",
    y_col: str = "shear_stress_Pa",
    grid: str = "log",
    n_grid: int = 200,
) -> Tuple[float, str]:
    """
    Compute hysteresis area A = ∫ |τ_up(γ̇)-τ_down(γ̇)| dγ̇
    after interpolating both curves on a common γ̇ grid.
    """
    xu = up[x_col].to_numpy(dtype=float)
    yu = up[y_col].to_numpy(dtype=float)
    xd = down[x_col].to_numpy(dtype=float)
    yd = down[y_col].to_numpy(dtype=float)

    masku = np.isfinite(xu) & np.isfinite(yu) & (xu > 0)
    maskd = np.isfinite(xd) & np.isfinite(yd) & (xd > 0)
    xu, yu = xu[masku], yu[masku]
    xd, yd = xd[maskd], yd[maskd]

    if xu.size < 3 or xd.size < 3:
        return float("nan"), "insufficient_points"

    # Sort for interpolation
    su = np.argsort(xu)
    sd = np.argsort(xd)
    xu, yu = xu[su], yu[su]
    xd, yd = xd[sd], yd[sd]

    xmin = max(float(np.min(xu)), float(np.min(xd)))
    xmax = min(float(np.max(xu)), float(np.max(xd)))
    if not (np.isfinite(xmin) and np.isfinite(xmax) and xmax > xmin > 0):
        return float("nan"), "no_overlap"

    if grid == "log":
        xs = np.logspace(np.log10(xmin), np.log10(xmax), n_grid)
        method = "interp_log_grid_trapz_dgamma"
    else:
        xs = np.linspace(xmin, xmax, n_grid)
        method = "interp_linear_grid_trapz_dgamma"

    yu_i = np.interp(xs, xu, yu)
    yd_i = np.interp(xs, xd, yd)

    area = float(np.trapz(np.abs(yu_i - yd_i), xs))
    return area, method


def compute_partial_yield(
    df: pd.DataFrame,
    tauy_Pa: float,
    r1_m: float,
    r2_m: float,
    *,
    tau1_col: str = "shear_stress_Pa",
) -> Tuple[float, np.ndarray, np.ndarray]:
    """
    Return (tau1c_Pa, r0_over_r2, partial_mask)

    tau1c = tauy*(r2/r1)^2
    partial if tau1 < tau1c

    r0 = min(r2, r1*sqrt(tau1/tauy))
    """
    tau1 = df[tau1_col].to_numpy(dtype=float)
    tau1c = float(tauy_Pa) * (float(r2_m) / float(r1_m)) ** 2

    r0_over_r2 = np.full_like(tau1, np.nan, dtype=float)
    partial = np.full_like(tau1, False, dtype=bool)

    if not (np.isfinite(tau1c) and tau1c > 0 and np.isfinite(tauy_Pa) and tauy_Pa > 0):
        return float("nan"), r0_over_r2, partial

    partial = (tau1 < tau1c) & np.isfinite(tau1)

    # r0/r2
    with np.errstate(invalid="ignore", divide="ignore"):
        r0 = float(r1_m) * np.sqrt(tau1 / float(tauy_Pa))
    r0 = np.minimum(r0, float(r2_m))
    r0_over_r2 = r0 / float(r2_m)
    return tau1c, r0_over_r2, partial


